$url = "https://beertest.blob.core.windows.net/public/install.zip"
$path = Join-Path -Path "." -ChildPath "temp"
$installerFilePath = Join-Path -Path $path -ChildPath "scripts.zip"

if ( (Test-Path -Path $path) -eq $false) {
    New-Item -Path $path -ItemType Directory
}

Invoke-WebRequest -Uri $url -OutFile $installerFilePath
Expand-Archive $installerFilePath -DestinationPath $path

foreach ($item in (Get-ChildItem -Path $path)) {
    if( ($item.Extension -Match ".zip")) {
        continue;
    }
    if ( ($item.Extension -Match ".json")) {
        if ( (Test-Path ( Join-Path -Path ".\" -ChildPath $item.Name )) -eq $false) {
            Move-Item -Path $item.FullName -Destination  "."
        }
    }
    else {
        Move-Item -Path $item.FullName -Destination  "." -Force
    }
}

Remove-Item -Path $path -Recurse -Force

